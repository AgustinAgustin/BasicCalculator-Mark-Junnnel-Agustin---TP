using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CalculatorPrivateAssembly
{
    public class BasicComputation
    {
        public static float Addition(float x, float y)
        {
            return x + y;
        }
        public static float Subtraction(float x, float y)
        {
            return x - y;
        }
        public static float Multiplication(float x, float y)
        {
            return x * y;
        }
        public static float Division(float x, float y)
        {
            return x / y;
        }
    }
}
